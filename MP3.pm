package Bundle::MP3;

$VERSION = '1.00';

1;

__END__

=head1 NAME

Bundle::MP3 - A bundle to install all MP3-related modules

=head1 SYNOPSIS

C<perl -MCPAN -e 'install Bundle::MP3'>

=head1 CONTENTS

MP3::Info               - For MP3::Info by Chris Sandor

Digest::MD5		- Used by MP3::Napster

MP3::Napster		- For MP3::Napster by Lincoln Stein

Bundle::Xmms            - For Xmms by Doug MacEachern

=head1 DESCRIPTION

This bundle installs a bunch of modules related to sharing and playing
MP3 files, including MP3::Info by Chris Sandor, MP3::Napster, by
Lincoln Stein, and the Xmms bundle by Doug MacEachern.

=head1 AUTHOR

Lincoln Stein E<lt>F<lstein@cshl.org>E<gt>

=cut
